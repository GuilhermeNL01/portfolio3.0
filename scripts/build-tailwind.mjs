import { compile } from 'tailwindcss';
import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const root = process.cwd();
const sourceDirs = ['src'];
const sourceFiles = ['index.html', 'public/curriculo.html'];
const outputFile = path.join(root, 'src/tailwind.generated.css');
const candidatePattern = /[A-Za-z0-9_!:/.[\]#%(),+-]+/g;

async function collectFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      files.push(...await collectFiles(fullPath));
      continue;
    }

    if (/\.(ts|tsx|html|css)$/.test(entry.name) && entry.name !== 'tailwind.generated.css') {
      files.push(fullPath);
    }
  }

  return files;
}

async function loadStylesheet(id, base) {
  const stylesheetPath = id === 'tailwindcss'
    ? path.join(root, 'node_modules/tailwindcss/index.css')
    : path.resolve(base, id);

  return {
    path: stylesheetPath,
    base: path.dirname(stylesheetPath),
    content: await readFile(stylesheetPath, 'utf8')
  };
}

const files = [
  ...sourceFiles.map((file) => path.join(root, file)),
  ...(await Promise.all(sourceDirs.map((dir) => collectFiles(path.join(root, dir))))).flat()
];

const candidates = new Set([
  'dark',
  'sr-only',
  'not-sr-only',
  'grid-border-b',
  'grid-border-r',
  'project-card',
  'project-img',
  'animate-fade-in',
  'animate-marquee',
  'code-line',
  'node-dot',
  'dot-inview',
  'mask-image-gradient',
  'bg-grid-12'
]);

for (const file of files) {
  const content = await readFile(file, 'utf8');
  for (const match of content.matchAll(candidatePattern)) {
    candidates.add(match[0]);
  }
}

const compiler = await compile(
  '@custom-variant dark (&:where(.dark, .dark *));\n@import "tailwindcss";',
  { base: root, loadStylesheet }
);

const css = compiler.build([...candidates]);
await writeFile(
  outputFile,
  `/* Generated by scripts/build-tailwind.mjs. Do not edit by hand. */\n${css}`,
  'utf8'
);
